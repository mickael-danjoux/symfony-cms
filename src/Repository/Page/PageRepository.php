/**
 * @extends ServiceEntityRepository<Page>
 *
 * @method Page|null find($id, $lockMode = null, $lockVersion = null)
 * @method Page|null findOneBy(array $criteria, array $orderBy = null)
 * @method Page[]    findAll()
 * @method Page[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 * @method Page|null findOneByPath(string $path)
 */
class PageRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Page::class);
    }

    /**
     * @throws Exception
     */
    public function findAllForRouting()
    {
        return $this->getEntityManager()
            ->getConnection()
            ->prepare('SELECT id, controller, path, route_name FROM page')
            ->executeQuery([])
            ->fetchAllAssociative();
    }

    public function findNextId()
    {
        return $this->createQueryBuilder('p')
                ->select('COALESCE(MAX(p.id), 0)')
                ->getQuery()
                ->getSingleScalarResult() + 1;
    }


    public function save(Page $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Page $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

